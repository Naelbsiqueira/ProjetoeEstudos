from math import sqrt, floor
num=int(input(('Digite um número:')))
raiz = sqrt(num)
print('A raiz de {} e igual a {:.2f}'.format(num,floor(raiz)))

#Vale ressaltar que é importante importa a bibliotca para ter a funcionalidade correpondente.
