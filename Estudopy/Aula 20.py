def titulo(txt):
    print('-='*25)
    print(txt)
    print('=-'*25)


titulo('               SISTEMA DE ALUNOS')


titulo('              CADASTRO DE FUNCIONÁRIOS')


titulo('                 ERRO DE SISTEMAS')
