#Funções ( ou também chamadas de ROTINA) são utilizada para minimizar utilização de código repetido.

print('=-'*25)
print('                SISTEMAS DE ALUNOS')
print('=-'*25)
print('=-'*25)
print('            CADASTRO DE FUNCIONÁRIOS')
